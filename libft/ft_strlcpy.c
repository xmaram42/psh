#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	srlen;
	size_t	i;

	srlen = ft_strlen(src);
	i = 0;
	if (dstsize == 0)
		return (srlen);
	while (src[i] && i < (dstsize - 1))
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (srlen);
}
